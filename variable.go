package main

import "fmt"

func main() {
	var name string

	name = "Dio Surandito"
	fmt.Println(name)

	name = "Surandito"
	fmt.Println(name)

	var friendName = "Bayu"
	fmt.Println(friendName)

	var age = 24
	fmt.Println(age)

	country := "Indonesia"
	fmt.Println(country)

	country = "Korea"
	fmt.Println(country)

	var (
		firstName = "Dio"
		lastName  = "Surandito"
	)
	fmt.Println(firstName)
	fmt.Println(lastName)
}
